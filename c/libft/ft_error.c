#include "./includes/libft.h"

void		ft_print_error(char *error)
{
	write(2, "\033[0;31;1m", 9);
	write(2, error, ft_strlen((const char *)error));
	write(2, "\033[0m", 4);
	write(2, "\n", 1);
}
